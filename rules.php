			<h3><b>Gameplay Basics:</b></h3><br/>
			<h4><b>The Sides:</b></h4>
			
			<br/><i>Regardless of your preferences, you must play your current side. Humans should not 'suicide' by running into a zombie
			horde, and zombies should not actively help humans with their objectives.</i><br/><br/>
			
			<h4><i>Human:</i></h4>
			<p>
			Humans wear a bandanna between their elbow and the shoulder and are equipped with blasters, socks, darts, etc.
			Humans attempt to complete mission objectives (outlined at the beginning of each mission) while surviving the horde
			by stunning zombies with their equipment.</p><br/>
			
			<h4><i>Zombie:</i></h4>
			<p>
			Unstunned zombies wear a bandanna around their head, while stunned zombies wear a bandanna around their neck - zombies are only
			armed with their bodies. Zombies attempt to prevent humans from completing their objectives by wiping them out. An
			*unstunned* zombie can kill a human by tagging them or their clothes, however, tagging a human's blaster does not count.
			</p><br/>
			
			<h4><i>Super-Humans:</i></h4>
			<p>
			When a human is killed by a zombie, they become a super-human and are 'dying'; they wear their bandannas as humans. Super-humans are not 
			considered human or zombie, and thus cannot complete human objectives or kill humans. However, super-humans may stun zombies or attempt 
			to sneak into a group of humans in order to kill them when they become a zombie. After a specified period of time, a super-human becomes
			an unstunned zombie. If a super-human is tagged by an unstunned zombie, they instantly become a stunned zombie for the remainder of 
			their death timer.
			</p>
			<br/>
			<h4><i>NOTE: Bandannas must be brightly-colored and contrast with worn clothing.  They must be visible from as many angles as possible
			and not be obscured by hair or clothing</i></h4>
			<br/><br/>
			
			<h4><b>Equipment:</b></h4>
			<i>The following equipment is allowed:</i>
			<ul>
			<li>All off-the-shelf NERF/Buzz Bee/Worker blasters</li>	
			<li>Modded NERF/Buzz Bee/Worker dart and disc blasters*</li>
			<li>All off-the-shelf NERF/Buzz Bee/Worker darts, discs, and balls*</li>
			<li>Modded NERF/Buzz Bee/Worker darts and discs*</li>
			<li>Rolled and taped socks</li>
			</ul>
			
			<i>The following equipment is <b>not</b> allowed:</i>
